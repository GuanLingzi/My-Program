import turtle

t = turtle.Pen()
t.pencolor('yellow')
t.pensize(10)
t.hideturtle()
t.pendown()
for i in range(6):
    t.forward(200)
    t.left(60)
t.begin_fill()
t.left(60)
t.forward(400)
t.penup()
t.left(120)
t.forward(200)
t.pendown()
t.left(120)
t.forward(400)
t.penup()
t.left(120)
t.forward(200)
t.left(120)
t.pendown()
t.forward(400)
t.end_fill()
turtle.done()
